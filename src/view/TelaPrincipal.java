    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int escolha = JOptionPane.showConfirmDialog(null, "Quer excluir", "AVISO!", JOptionPane.WARNING_MESSAGE);
        PacienteDAO pacienteDao = new PacienteDAO();
        if (escolha == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(txtSiso.getText());
            pacienteDao.excluir(id);
            carregaTabela();
            txtNome.setText("");
            txtData.setText("");
            txtCpf.setText("");
            txtSiso.setText("");
            txtEmail.setText("");
            txtEndereco.setText("");
            txtCelular.setText("");
            txtObservacao.setText("");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Pacientes paciente = new Pacientes();
        PacienteDAO pacienteDao = new PacienteDAO();
        if (txtNome.getText().equals("") || txtData.getText().equals("") || txtCpf.getText().equals("") || txtEmail.getText().equals("") || txtEndereco.getText().equals("") || txtCelular.getText().equals("")) {
            JOptionPane.showMessageDialog(null, " Campos em branco", "AVISO!", JOptionPane.WARNING_MESSAGE);
            return;
        } else if (txtSiso.getText().equals("")) {
            paciente.setNome(txtNome.getText());
            SimpleDateFormat simpleDateFormat = new SimpleDateFormat(pattern);
            try {
                paciente.setData_Nascimento(simpleDateFormat.parse(txtData.getText()));
            } catch (ParseException ex) {
                Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            paciente.setCpf(txtCpf.getText());
            paciente.setEmail(txtEmail.getText());
            paciente.setEndereco(txtEndereco.getText());
            paciente.setCelular(txtCelular.getText());
            paciente.setObservacao(txtObservacao.getText());
            Long id = pacienteDao.salvar(paciente);
            txtSiso.setText(String.valueOf(id));
        }
        carregaTabela();
        txtNome.setText("");
        txtData.setText("");
        txtCpf.setText("");
        txtSiso.setText("");
        txtEmail.setText("");
        txtEndereco.setText("");
        txtCelular.setText("");
        txtObservacao.setText("");
    }//GEN-LAST:event_btnSalvarActionPerformed

