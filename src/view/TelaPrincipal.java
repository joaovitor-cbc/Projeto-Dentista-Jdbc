    private void tblTabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTabelaMouseClicked
        if (tblTabela.getSelectedRow() != -1) {
            int linha = tblTabela.getSelectedRow();
            DefaultTableModel tabelaModel = (DefaultTableModel) tblTabela.getModel();
            int siso = (int) tabelaModel.getValueAt(linha, 0);
            String nome = (String) tabelaModel.getValueAt(linha, 1);
            String cpf = (String) tabelaModel.getValueAt(linha, 2);
            Date data = (Date) tabelaModel.getValueAt(linha, 3);
            String email = (String) tabelaModel.getValueAt(linha, 4);
            String celular = (String) tabelaModel.getValueAt(linha, 5);
            String endereco = (String) tabelaModel.getValueAt(linha, 6);
            String observacao = (String) tabelaModel.getValueAt(linha, 7);
            txtSiso.setText(Integer.toString(siso));
            txtNome.setText(nome);
            txtCpf.setText(cpf);
            SimpleDateFormat simpleDateFormat = new SimpleDateFormat(pattern);
            txtData.setText(simpleDateFormat.format(data));
            txtEmail.setText(email);
            txtCelular.setText(celular);
            txtEndereco.setText(endereco);
            txtObservacao.setText(observacao);
        }
    }//GEN-LAST:event_tblTabelaMouseClicked
    public void carregaTabela() {
        limparTabela();
        PacienteDAO pacienteDao = new PacienteDAO();
        DefaultTableModel tabelaModel = (DefaultTableModel) tblTabela.getModel();
        List<Pacientes> lista = new ArrayList<>();
        lista = pacienteDao.listaTodos();
        for (Pacientes p : lista) {
            Object[] objeto = new Object[]{p.getSiso(), p.getNome(), p.getCpf(), p.getData_Nascimento(), p.getEmail(), p.getCelular(), p.getEndereco(), p.getObservacao()};
            tabelaModel.addRow(objeto);
        }
    }

    public void limparTabela() {
        DefaultTableModel tabelaModel = (DefaultTableModel) tblTabela.getModel();
        while (tabelaModel.getRowCount() > 0) {
            tabelaModel.removeRow(0);
        }
    }

